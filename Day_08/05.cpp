/*8位二进制数代表的十进制数*/
# include <stdio.h>

int main(void)
{
	char ch = 0X80;  
	/*
		对应的二进制数即为 1000 0000
		字符占用1字节，即8位
	 */
	printf("%d\n", ch);

	ch = 128;
	/*
		128对应二进制数为 1000 0000
		整形占用4字节，赋值给字符型时，高3字节会丢失
		所有字符型变量ch中存放的是 1000 0000
	*/
	printf("%d\n", ch);

	return 0;
}
